         T.TARGETMARKET = S.TARGETMARKET, T.HOLDERSNAME = S.HOLDERSNAME, T.GOODSTYPE = S.GOODSTYPE, T.PACKTYPE = S.PACKTYPE,
         T.BOMID = S.BOMID, T.LASTSUQTY = S.LASTSUQTY2, T.TRANPOSID = S.TRANPOSID, T.ISERP = 1, T.UPDATE_TIME = SYSTIMESTAMP, T.UPDATE_BY = 'ERP_SYNC', T.DELETED = 0
    WHEN NOT MATCHED THEN INSERT (GOODSID, GOODSNO, GOODSNAME, PRICE, SUQTY, USEFLAG, FACTORYNAME, STANDARDTYPE, ZX_PL, ZX_MINIMUM, APPROVEDOCNO, BASEUNITQTY, ZX_CUSTOMERID, TARGETMARKET, HOLDERSNAME, ISERP, GOODSTYPE, PACKTYPE, BOMID, LASTSUQTY, TRANPOSID, DELETED, CREATE_TIME, UPDATE_TIME, CREATE_BY, UPDATE_BY)
