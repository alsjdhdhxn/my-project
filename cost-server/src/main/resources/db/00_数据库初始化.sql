-- 清理视图
BEGIN
  FOR v IN (SELECT view_name FROM user_views WHERE view_name LIKE 'T_COST_%' OR view_name LIKE 'V_COST_%') LOOP
    EXECUTE IMMEDIATE 'DROP VIEW ' || v.view_name;
  END LOOP;
END;
/

-- 清理表
BEGIN
  FOR t IN (
    SELECT table_name
      FROM user_tables
     WHERE table_name LIKE 'T_COST_%'
        OR table_name LIKE 'MV_COST_%'
        OR table_name LIKE 'T_WMS_%'
        ) LOOP
    EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
  END LOOP;
END;
/

-- 清理序列
BEGIN
  FOR s IN (
    SELECT sequence_name
      FROM user_sequences
     WHERE sequence_name LIKE 'SEQ_COST_%'
        OR sequence_name LIKE 'ISEQ$$_%'
        OR sequence_name LIKE 'SEQ_WMS_%'
        ) LOOP
    EXECUTE IMMEDIATE 'DROP SEQUENCE ' || s.sequence_name;
  END LOOP;
END;
/

