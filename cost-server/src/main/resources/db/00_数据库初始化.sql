-- 同步客户数据存储过程
CREATE OR REPLACE PROCEDURE PROC_SYNC_CUSTOMER AS
BEGIN
    MERGE INTO T_COST_CUSTOMER t USING (SELECT CUSTOMID, CUSTOMNAME, ZONE FROM PUB_CUSTOMER@hyerp) s
    ON (t.CUSTOMID = s.CUSTOMID)
    WHEN MATCHED THEN UPDATE SET t.CUSTOMNAME = s.CUSTOMNAME, t.ZONE = s.ZONE,
                                 t.UPDATE_TIME = SYSTIMESTAMP, t.ISERP = 1, t.DELETED = 0
    WHEN NOT MATCHED THEN INSERT (CUSTOMID, CUSTOMNAME, ZONE, ISERP, DELETED, CREATE_BY, CREATE_TIME)
                          VALUES (s.CUSTOMID, s.CUSTOMNAME, s.ZONE, 1, 0, 'sync', SYSTIMESTAMP);
    MERGE INTO T_COST_TRANPOSER t USING (SELECT TRANPOSID, COMPANYID AS CUSTOMID, TRANPOSNAME FROM BMS_TR_POS_DEF@hyerp) s
    ON (t.TRANPOSID = s.TRANPOSID)
    WHEN MATCHED THEN UPDATE SET t.CUSTOMID = s.CUSTOMID, t.TRANPOSNAME = s.TRANPOSNAME,
                                 t.UPDATE_TIME = SYSTIMESTAMP, t.ISERP = 1, t.DELETED = 0
    WHEN NOT MATCHED THEN INSERT (TRANPOSID, CUSTOMID, TRANPOSNAME, ISERP, DELETED, CREATE_BY, CREATE_TIME)
                          VALUES (s.TRANPOSID, s.CUSTOMID, s.TRANPOSNAME, 1, 0, 'sync', SYSTIMESTAMP);
    COMMIT;
END;
